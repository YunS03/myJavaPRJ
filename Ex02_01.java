public class Ex02_01 {

    public static void main(String[] args) {
        int a,b;
        int result;

        a=100;
        b=50;

        result=a+b;
        System.out.println(a+"+"+b+"="+result);
        result=a-b;
        System.out.println(a+"-"+b+"="+result);
        result=a*b;
        System.out.println(a+"*"+b+"="+result);
        result=a/b;
        System.out.println(a+"/"+b+"="+result);
    }
}

//100 과 50 덧셈 뺼셈 곱하기 나눈기 수행 코드